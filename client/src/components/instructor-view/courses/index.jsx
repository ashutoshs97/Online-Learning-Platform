import { Button } from "@/components/ui/button";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "@/components/ui/table";
import {
  courseCurriculumInitialFormData,
  courseLandingInitialFormData,
} from "@/config";
import { InstructorContext } from "@/context/instructor-context";
import { Delete, Edit } from "lucide-react";
import { useContext } from "react";
import { useNavigate } from "react-router-dom";

function InstructorCourses({ listOfCourses }) {
  const navigate = useNavigate();
  const {
    setCurrentEditedCourseId,
    setCourseLandingFormData,
    setCourseCurriculumFormData,
  } = useContext(InstructorContext);

  return (
    <Card>
      <CardHeader className="flex justify-between flex-row items-center">
        <CardTitle className="text-3xl font-extrabold">All Courses</CardTitle>
        <Button
          onClick={() => {
            setCurrentEditedCourseId(null);
            setCourseLandingFormData(courseLandingInitialFormData);
            setCourseCurriculumFormData(courseCurriculumInitialFormData);
            navigate("/instructor/create-new-course");
          }}
          className="p-6 bg-teal-600 text-white hover:bg-teal-700 transition duration-200"
        >
          Create New Course
        </Button>
      </CardHeader>
      <CardContent>
        <div className="overflow-x-auto">
          <Table className="min-w-full bg-white shadow-md rounded-lg">
            <TableHeader>
              <TableRow className="bg-teal-100">
                <TableHead className="py-3 px-4 text-left font-semibold text-teal-700">
                  Course
                </TableHead>
                <TableHead className="py-3 px-4 text-left font-semibold text-teal-700">
                  Students
                </TableHead>
                <TableHead className="py-3 px-4 text-left font-semibold text-teal-700">
                  Revenue
                </TableHead>
                <TableHead className="py-3 px-4 text-right font-semibold text-teal-700">
                  Actions
                </TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {listOfCourses && listOfCourses.length > 0
                ? listOfCourses.map((course) => (
                    <TableRow
                      key={course._id}
                      className="hover:bg-teal-50 transition duration-200"
                    >
                      <TableCell className="font-medium py-3 px-4">
                        {course?.title}
                      </TableCell>
                      <TableCell className="py-3 px-4">
                        {course?.students?.length}
                      </TableCell>
                      <TableCell className="py-3 px-4">
                        ${course?.students?.length * course?.pricing}
                      </TableCell>
                      <TableCell className="text-right py-3 px-4">
                        <Button
                          onClick={() => {
                            navigate(`/instructor/edit-course/${course?._id}`);
                          }}
                          variant="ghost"
                          size="sm"
                          className="transform transition-all duration-200 hover:scale-110 hover:text-teal-600"
                        >
                          <Edit className="h-6 w-6" />
                        </Button>
                        <Button
                          variant="ghost"
                          size="sm"
                          className="ml-2 transform transition-all duration-200 hover:scale-110 hover:text-red-600"
                        >
                          <Delete className="h-6 w-6" />
                        </Button>
                      </TableCell>
                    </TableRow>
                  ))
                : null}
            </TableBody>
          </Table>
        </div>
      </CardContent>
    </Card>
  );
}

export default InstructorCourses;